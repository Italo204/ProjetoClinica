    private void cadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarButtonActionPerformed
        String nome = nomeTextField.getText();
        String email = emailtextField.getText();
        String CPF = cpfTextField.getText().replaceAll("[^0-9]", "");
        String senha = BCrypt.hashpw(senhaTextField.getText(), BCrypt.gensalt());
        String nascimentoNoFormat = nascimentoTextField.getText();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate nascimento = LocalDate.parse(nascimentoNoFormat, formatter);
        String gender = mapearGenero(cbGender.getSelectedItem().toString());
        String telefone = telefoneTextField.getText();
        
        Atendente atendente = new Atendente(nome, email, senha, CPF, telefone, gender, nascimento);
        AtendenteController atenCon = new AtendenteController();
        atenCon.saveAtendente(atendente);
        
    }//GEN-LAST:event_cadastrarButtonActionPerformed

    private static String mapearGenero(String genero) {
         if ("MASCULINO".equals(genero)) {
            return "M";
        } else if ("FEMININO".equals(genero)) {
            return "F";
        }
        return genero;
    }

    private void configureCpfField(){
        try {
            MaskFormatter mask = new MaskFormatter("###.###.###-##");
            

            mask.install(cpfTextField);
            cpfTextField.setColumns(14);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
